void pg_opt_init(pg_opt_t *o)
{
	memset(o, 0, sizeof(pg_opt_t));
	o->k = 31;
	o->pre = 10;
	o->n_threads = 4;
	o->chunk_size = mm_parse_num("1.9g");
	o->verbose = 1;
}

int64_t mm_parse_num(const char *str)
{
    double x;
    char *p;
    x = strtod(str, &p);
    if      (*p == 'G' || *p == 'g') x *= 1e9;
    else if (*p == 'M' || *p == 'm') x *= 1e6;
    else if (*p == 'K' || *p == 'k') x *= 1e3;
    return (int64_t)(x + .499);
}